#include "libft.h"

int		ft_atoi(const char *str)
{
	int r;
	int neg;

	r = 0;
	neg = 1;
	while (*str == ' ' || *str == '\n' || *str == '\r' || *str == '\t'
			|| *str == '\v' || *str == '\f')
		str++;
	if (*str == '-' || *str == '+')
	{
		if (*str == '-')
			neg = -1;
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		r = r * 10 + *str - '0';
		str++;
	}
	return (r * neg);
}
